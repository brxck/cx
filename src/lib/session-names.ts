/** Generate a session name from PNW towns, avoiding names already used for this workspace. */
export function generateSessionName(existing: string[]): string {
  const available = PNW_TOWNS.filter((t) => !existing.includes(t));
  const pool = available.length > 0 ? available : PNW_TOWNS;
  return pool[Math.floor(Math.random() * pool.length)]!;
}
